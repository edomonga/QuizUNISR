export function getQuestionsByMacroArea(macroArea: MacroArea): Question[] {
  return questions.filter(q => q.macroArea === macroArea);
}

export function getQuestionsByTopic(topic: string): Question[] {
  return questions.filter(q => q.topic === topic);
}

export function getRandomQuestions(macroAreas: MacroArea[], count: number): Question[] {
  const pool = questions.filter(q => macroAreas.includes(q.macroArea));
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getExamQuestions(): Question[] {
  const getRandomFromArea = (area: MacroArea, count: number): Question[] => {
    const pool = questions.filter(q => q.macroArea === area);
    return [...pool].sort(() => Math.random() - 0.5).slice(0, Math.min(count, pool.length));
  };
  return [
    ...getRandomFromArea('igiene', 12),
    ...getRandomFromArea('medicina_legale', 12),
    ...getRandomFromArea('medicina_del_lavoro', 3),
    ...getRandomFromArea('economia_sanitaria', 3),
  ].sort(() => Math.random() - 0.5);
}
